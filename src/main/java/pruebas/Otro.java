package pruebas;

public class Otro extends Usr {
	private Double d;
	private double d2;

	public Otro(Double d, double d2) {
		this.d = d;
		this.d2 = d2;
	}

	public Double getD() {
		return d;
	}

	public void setD(Double d) {
		this.d = d;
	}

	public double getD2() {
		return d2;
	}

	public void setD2(double d2) {
		this.d2 = d2;
	}

}
